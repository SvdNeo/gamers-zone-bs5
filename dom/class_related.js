// Adding CSS using JavaScript:
// Selecting an element by ID and modifying its CSS property
document.getElementById('myElement').style.color = 'red';

// Manipulating classes using classList:
// Adding a class to an element
document.getElementById('myElement').classList.add('highlight');

// Removing a class from an element
document.getElementById('myElement').classList.remove('highlight');

// Adding a class to an element using className property
document.getElementById('myElement').className += ' highlight';

// Modifying the innerHTML of an element
document.getElementById('myElement').innerHTML = '<p>This is the updated content.</p>';

// Using document.write() to write content directly to the document
document.write('<h1>Welcome to my website!</h1>');

// Using innerHTML to modify the content of an element
document.getElementById('myElement').innerHTML = '<h1>Welcome to my website!</h1>';

